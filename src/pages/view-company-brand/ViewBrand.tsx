import { FontAwesomeIcon } from "@fortawesome/react-fontawesome";
import "./ViewCompanyBrand.scss";
import { faEdit, faTrash } from "@fortawesome/free-solid-svg-icons";
import InputBox, { InputTypes } from "../../components/input-box/InputBox";
import { useEffect, useState } from "react";
import EmptyPage from "../../components/empty-page/EmptyPage";

const ViewBrand = () => {
  const [showEmptyScreen, setShowEmptyScreen] = useState(false);
  const [companyId, setCompanyId] = useState("");

  useEffect(() => {
    const cards = document.querySelectorAll(".card");

    const companyName =
      data.find((data) => data.id === companyId)?.companyName ?? "";

    if (cards.length) {
      cards.forEach((card) => {
        const cardCompanyTitleElement = card.querySelector("#company-title");

        if (cardCompanyTitleElement) {
          const companyCardTitle =
            cardCompanyTitleElement?.textContent?.toLowerCase();

          if (
            companyCardTitle &&
            companyCardTitle.includes(companyName.toLowerCase())
          ) {
            (card as HTMLElement).style.display = "block";
          } else {
            (card as HTMLElement).style.display = "none";
          }

          const allHidden = Array.from(cards).every(
            (card) => (card as HTMLElement).style.display === "none"
          );

          if (allHidden) {
            setShowEmptyScreen(true);
          } else {
            setShowEmptyScreen(false);
          }
        }
      });
    }
  }, [companyId]);

  return (
    <div className="container">
      <div className="list-header">
        <h1>Brands List</h1>
        <div className="search-bar">
          <InputBox
            id="company-name"
            name="company-name"
            type={InputTypes.DropDown}
            label="Company name"
            value={companyId}
            options={
              [
                // {
                //   id: "2",
                //   title: "GreenSolutions",
                // },
                // {
                //   id: "3",
                //   title: "FinanceWise",
                // },
              ]
            }
            isRequired
            setInputValue={setCompanyId}
          />
        </div>
      </div>
      <div className="grid">
        {data.length > 0 &&
          data.map((data) => (
            <div className="card" key={data.id}>
              <img src={data.logoLink} alt="TechCorp Logo" />
              <div className="card-footer">
                <div className="title">
                  <p id="brand-title">{data.brandName}</p>
                  <p id="company-title" className="company-name">
                    {data.companyName}
                  </p>
                </div>
                <div className="icons">
                  <FontAwesomeIcon icon={faEdit} />
                  <FontAwesomeIcon icon={faTrash} />
                </div>
              </div>
            </div>
          ))}
        {(data.length === 0 || showEmptyScreen) && <EmptyPage />}
      </div>
    </div>
  );
};

export default ViewBrand;
